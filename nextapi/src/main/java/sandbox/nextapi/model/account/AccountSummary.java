package sandbox.nextapi.model.account;

public class AccountSummary {
	
	private String accountCurrency;
	
	private Double accountSum;
	
	private Double collateral;
	
	private Double forwardSum;
	
	private Double fullMarketValue;
	
	private Double futureSum;
	
	private Boolean hasBankServices;
	
	private Double interest;
	
	private Double loanLimit;
	
	private Double ownCapital;
	
	private Double ownCapitalMorning;
	
	private Double pawnValue;
	
	private Double tradingPower;

	public String getAccountCurrency() {
		return accountCurrency;
	}

	public void setAccountCurrency(String accountCurrency) {
		this.accountCurrency = accountCurrency;
	}

	public Double getAccountSum() {
		return accountSum;
	}

	public void setAccountSum(Double d) {
		this.accountSum = d;
	}

	public Double getCollateral() {
		return collateral;
	}

	public void setCollateral(Double collateral) {
		this.collateral = collateral;
	}

	public Double getForwardSum() {
		return forwardSum;
	}

	public void setForwardSum(Double forwardSum) {
		this.forwardSum = forwardSum;
	}

	public Double getFullMarketValue() {
		return fullMarketValue;
	}

	public void setFullMarketValue(Double fullMarketValue) {
		this.fullMarketValue = fullMarketValue;
	}

	public Double getFutureSum() {
		return futureSum;
	}

	public void setFutureSum(Double futureSum) {
		this.futureSum = futureSum;
	}

	public Boolean getHasBankServices() {
		return hasBankServices;
	}

	public void setHasBankServices(Boolean hasBankServices) {
		this.hasBankServices = hasBankServices;
	}

	public Double getInterest() {
		return interest;
	}

	public void setInterest(Double interest) {
		this.interest = interest;
	}

	public Double getLoanLimit() {
		return loanLimit;
	}

	public void setLoanLimit(Double loanLimit) {
		this.loanLimit = loanLimit;
	}

	public Double getOwnCapital() {
		return ownCapital;
	}

	public void setOwnCapital(Double ownCapital) {
		this.ownCapital = ownCapital;
	}

	public Double getOwnCapitalMorning() {
		return ownCapitalMorning;
	}

	public void setOwnCapitalMorning(Double ownCapitalMorning) {
		this.ownCapitalMorning = ownCapitalMorning;
	}

	public Double getPawnValue() {
		return pawnValue;
	}

	public void setPawnValue(Double pawnValue) {
		this.pawnValue = pawnValue;
	}

	public Double getTradingPower() {
		return tradingPower;
	}

	public void setTradingPower(Double tradingPower) {
		this.tradingPower = tradingPower;
	}
	
	
}
